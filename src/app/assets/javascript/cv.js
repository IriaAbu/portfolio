$('#iconCpt').click(function () {
    $('.slideCompetences').css('display', 'block')
    $('.slideExp').css('display', 'none')
    $('.slideDip').css('display', 'none')
})

$('#iconExp').click(function () {
    $('.slideExp').css('display', 'block')
    $('.slideCompetences').css('display', 'none')
    $('.slideDip').css('display', 'none')
})

$('#iconDip').click(function () {
    $('.slideDip').css('display', 'block')
    $('.slideCompetences').css('display', 'none')
    $('.slideExp').css('display', 'none')
})